    // ============================================================
    // TC_05_05 - Verify double payment prevention
    // ============================================================

    @Test(description = "TC_05_05 - Double payment prevention",
            groups = {"regression", "payment"})
    public void TC_05_05_preventDoublePayment() {
        createTest("TC_05_05", "Double payment prevention");
        getTest().info("Verify UI logic - completed payments don't have Complete button");
        
        loginAsBrand();
        navbar.goToPayments();

        int totalRows = paymentPage.getPaymentRowCount();
        int completeButtons = paymentPage.getCompleteButtonCount();
        
        // If there are completed payments, the number of Complete buttons should be less than total rows
        if (totalRows > 0 && paymentPage.getPaymentStatuses().contains("Completed")) {
            Assert.assertTrue(completeButtons < totalRows, 
                "Completed payments should not have a Complete button to prevent double processing");
            getTest().pass("Completed payments correctly disable/hide the processing button");
        } else {
            getTest().skip("No completed payments available to verify logic");
        }
    }

    // ============================================================
    // TC_05_06 - Verify influencer dashboard updates total earnings
    // ============================================================

    @Test(description = "TC_05_06 - Influencer dashboard updates total earnings",
            groups = {"regression", "payment"})
    public void TC_05_06_influencerDashboardEarnings() {
        createTest("TC_05_06", "Influencer dashboard updates total earnings");
        loginAsInfluencer();
        
        getTest().info("Step 1: Check Total Earnings metric on dashboard");
        String earningsStr = influencerDashboard.getTotalEarnings();
        Assert.assertNotNull(earningsStr, "Earnings should not be null");
        
        // Check if it parses as a number (removing $ and commas)
        try {
            String cleanNumber = earningsStr.replaceAll("[^0-9.]", "");
            double earnings = Double.parseDouble(cleanNumber);
            getTest().pass("Dashboard correctly calculates and displays earnings metric: " + earningsStr);
        } catch (NumberFormatException e) {
            Assert.fail("Earnings is not a valid number format: " + earningsStr);
        }
    }
