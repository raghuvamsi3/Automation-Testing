    // TC_05_01 - Verify payment auto-creation upon work completion
    // ============================================================

    @Test(description = "TC_05_01 - Payment auto-creation in PENDING status",
            groups = {"regression", "payment"})
    public void TC_05_01_paymentAutoCreation() {
        createTest("TC_05_01", "Payment auto-creation upon work completion");
        getTest().info("Note: True end-to-end requires driving a campaign from creation to completion. Validating PENDING payments exist.");
        loginAsBrand();
        navbar.goToPayments();

        getTest().info("Verify PENDING payments exist in the table");
        java.util.List<String> statuses = paymentPage.getPaymentStatuses();
        boolean hasPending = statuses.stream().anyMatch(s -> s.equalsIgnoreCase("Pending"));
        
        if (hasPending) {
            getTest().pass("Found PENDING payments automatically generated by system.");
        } else {
            getTest().skip("No PENDING payments found. Cannot verify auto-creation without full E2E setup.");
        }
    }

    // ============================================================
    // TC_05_02 - Verify brand can confirm pending payment
    // ============================================================

    @Test(description = "TC_05_02 - Brand can process and confirm payment",
            groups = {"smoke", "regression", "payment"})
    public void TC_05_02_confirmPendingPayment() {
        createTest("TC_05_02", "Confirm pending payment");
        loginAsBrand();
        navbar.goToPayments();

        if (!paymentPage.hasCompleteButton()) {
            getTest().skip("No pending payments available to complete");
            throw new SkipException("No pending payments");
        }

        getTest().info("Step 1: Click Complete on the first pending payment");
        paymentPage.completeFirstPayment();

        getTest().info("Step 2: Verify success message");
        String snackbarText = paymentPage.getSnackbarText();
        Assert.assertTrue(snackbarText.toLowerCase().contains("success") || 
                          snackbarText.toLowerCase().contains("complet"),
                "Should show success message on completion");

        getTest().pass("Payment confirmed successfully");
    }

